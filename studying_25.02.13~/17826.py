# https://www.acmicpc.net/problem/17826문제
# 3학년인 홍익이는 이번 학기 전공필수 과목인 운영체제(OS) 수업을 들었다. 수업을 마치고, 얼마 후 교수님께서 클래스넷을 통해 전 학생의 중간고사, 기말고사, 과제점 점수를 만점 기준 300점으로 환산하여 클래스넷에 올려주셨다. 물끄러미 점수를 바라보던 홍익이는, 불현듯 장학금을 꼭 받아야 된다는 사실이 떠올랐다.

# "운영체제 수업을 A0 이상 받지 못하면, 장학금을 받기 어려운데..."

# 교수님께서는 운영체제 첫 수업 시간에 학점 분포도를 다음과 같이 설명하셨다.

# A+: 1~5등
# A0: 6~15등
# B+: 16~30등
# B0: 31~35등
# C+: 36~45등
# C0: 46~48등
# F: 49~50등
# 교수님께서는 클래스넷의 수시정보 페이지에 학생들의 최종 점수를 내림차순으로 정렬하여 점수를 보여주셨다.

# 10일 뒤면 클래스넷을 통해 학점이 공지되지만, 그때까지 참기 어려웠던 홍익이는 프로그램을 통해 자신의 학점을 알아보고자 한다. 홍익이를 도와 홍익이의 학점을 구해보자!

# 입력
# 첫째 줄에는 홍익이의 점수를 포함한, 학생들의 점수 50개가 띄어쓰기로 구분되어 주어진다. 점수는 내림차순으로 정렬되어있고, 같은 점수는 존재하지 않는다.

# 둘째 줄에는 홍익이가 받은 점수가 주어진다.

# 모든 점수는 0 이상 300 이하의 정수이다.

# 출력
# 첫째 줄에 홍익이의 학점을 출력하시오.

# 예제 입력 1 
# 285 271 270 268 264 251 237 236 228 227 226 225 224 217 216 205 198 193 192 190 182 168 165 161 157 146 141 135 127 126 122 114 105 81 80 76 70 67 63 59 55 44 34 24 19 14 9 5 2 1
# 251
# 예제 출력 1 
# A0
# 예제 입력 2 
# 299 293 291 288 287 286 278 276 275 269 262 260 244 239 236 232 213 209 204 199 197 184 179 178 175 170 167 166 162 158 141 130 128 124 115 111 108 105 63 60 57 54 49 44 40 34 17 11 4 3
# 4
# 예제 출력 2 
# F
# 예제 입력 3 
# 300 293 292 289 285 281 273 271 261 255 236 233 230 226 224 219 216 210 207 206 193 191 190 182 169 163 161 159 157 154 153 148 143 137 134 121 108 100 87 76 71 59 46 45 38 25 20 4 3 1
# 148
# 예제 출력 3 
# B0


list1 = list(map(int, input().split()))

a = int(input())

a2 = list1.index(a)  + 1
ac = ''




if 1 <= a2 <= 5:
    ac = 'A+'
elif 6 <= a2 <= 15:
    ac = 'A0'

elif 16 <= a2 <= 30:
    ac = 'B+'
elif 31 <= a2 <= 35:
    ac = 'B0'
elif 36 <= a2 <= 45:
    ac = 'C+'
elif 46 <= a2 <= 48:
    ac = 'C0'
elif  49 <= a2 <= 50:
    ac = 'F'

print(ac)